import {Router} from 'express'; const r=Router();
r.get('/',(_,res)=>res.json({message:'API Running'}));
r.get('/categories',(_,res)=>res.json([{id:1,name:'Food'}]));
r.post('/orders',(_,res)=>res.json({message:'Order created'}));
r.post('/admin/login',(_,res)=>res.json({token:'sample-jwt-token'}));
r.post('/admin/forgot-password',(_,res)=>res.json({message:'Reset link sent'}));
r.post('/admin/reset-password',(_,res)=>res.json({message:'Password reset'}));
r.get('/admin/orders',(_,res)=>res.json([]));
r.get('/admin/orders/:id',(_,res)=>res.json({id:1}));
r.put('/admin/orders/:id',(_,res)=>res.json({message:'Order updated'}));
r.get('/admin/products',(_,res)=>res.json([]));
r.post('/admin/products',(_,res)=>res.json({message:'Product created'}));
r.put('/admin/products/:id',(_,res)=>res.json({message:'Product updated'}));
r.delete('/admin/products/:id',(_,res)=>res.json({message:'Product deleted'}));
r.get('/admin/categories',(_,res)=>res.json([]));
r.post('/admin/categories',(_,res)=>res.json({message:'Category created'}));
r.put('/admin/categories/:id',(_,res)=>res.json({message:'Category updated'}));
r.delete('/admin/categories/:id',(_,res)=>res.json({message:'Category deleted'}));
r.get('/admin/users',(_,res)=>res.json([]));
r.post('/admin/users',(_,res)=>res.json({message:'Admin created'}));
r.put('/admin/users/:id',(_,res)=>res.json({message:'Admin updated'}));
r.delete('/admin/users/:id',(_,res)=>res.json({message:'Admin deleted'}));
